package exercicio1;

public class Crianca {

	private String nome;
	private Personalidades personalidade;

	public Crianca(String nome, Personalidades personalidade) {
		super();
		this.nome = nome;
		this.personalidade = personalidade;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Personalidades getPersonalidade() {
		return personalidade;
	}

	public void setPersonalidade(Personalidades personalidade) {
		this.personalidade = personalidade;
	}

}
	
	
	

